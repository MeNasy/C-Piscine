#include <stdio.h>
#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	i;
	int	*dizi;
	int	ac;

	if (min >= max)
		return (0);
	ac = max - min;
	dizi = malloc(sizeof(int) * (ac));
	if (dizi == NULL)
		return (NULL);
	i = 0;
	while (max > min)
	{
		dizi[i] = min;
		min++;
		i++;
	}
	return (dizi);
}

#include <stdlib.h>
#include <stdio.h>

char	*ft_strdup(char	*src)
{
	int		uz;
	int		i;
	char	*dest;

	uz = 0;
	i = 0;
	while (src[uz])
		uz++;
	dest = malloc(sizeof(char) * (uz + 1));
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

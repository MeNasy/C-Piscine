#include <stdlib.h>

char	*ft_strjoin(int size, char **strs, char *sep)
{
	int		i;
	int		j;
	int		k;
	char	*dizi;

	k = 0;
	i = 0;
	dizi = malloc(sizeof(strs));
	while (i < size)
	{
		j = 0;
		while (strs[i][j] != '\0')
			dizi[k++] = strs[i][j++];
		j = 0;
		while (sep[j] != '\0' && i != size - 1)
			dizi[k++] = sep[j++];
		i++;
	}
	dizi[k] = '\0';
	return (dizi);
}

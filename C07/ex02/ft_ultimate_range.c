#include <stdlib.h>
#include <stdio.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;
	int	*arry;
	int	deger;

	i = 0;
	if (min >= max)
	{
		range = NULL;
		return (0);
	}
	deger = max - min;
	arry = malloc (sizeof(int) * (deger));
	if (arry == NULL)
	{
		range = NULL;
		return (-1);
	}
	*range = arry;
	while (min < max)
	{
		arry[i] = min;
		i++;
		min++;
	}
	return (i);
}

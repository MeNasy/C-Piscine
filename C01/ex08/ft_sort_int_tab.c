void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	bos;

	j = 0;
	while (j < size)
	{
		i = 0;
		while (i < size - 1)
		{
			if (tab[i + 1] < tab[i])
			{
				bos = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = bos;
			}
		i++;
		}
		j++;
	}
}

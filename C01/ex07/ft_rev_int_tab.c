void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	bos;

	i = 0;
	j = size - 1;
	while (i < size / 2)
	{
		bos = tab[i];
		tab[i] = tab[j - i];
		tab[j - i] = bos;
		i++;
	}
}

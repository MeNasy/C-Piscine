int	prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	kontrol;

	while (1)
	{
		kontrol = prime(nb);
		if (kontrol == 1)
			return (nb);
		else
			nb++;
	}
}

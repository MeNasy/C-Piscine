char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	uz;

	i = 0;
	uz = 0;
	while (dest[uz])
		uz++;
	while (src[i])
	{
		dest[uz] = src[i];
		i++;
		uz++;
	}
	dest[uz] = '\0';
	return (dest);
}
